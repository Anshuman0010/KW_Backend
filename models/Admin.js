const mongoose = require('mongoose');

const adminSchema = new mongoose.Schema({
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  isActive: {
    type: Boolean,
    default: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  },
  lastLogin: {
    type: Date
  }
});

// Add method to check if admin is active
adminSchema.methods.isActiveAdmin = function() {
  return this.isActive;
};

module.exports = mongoose.model('Admin', adminSchema); 